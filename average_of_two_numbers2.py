# Another program to find the average of two numbers.

def main():
    print("This program prints the average of two numbers.")
    s1 = eval(input("Enter your first score: "))
    s2 = eval(input("Enter your second score: "))
    average = (s1 + s2) / 2
    print("The average of your scores is", average)

main()
